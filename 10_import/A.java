// This show basic use of java.lang classes which we have not to import in our class because compiler imports java.lang package internally for us

class A {
    static String a;
    static System b;
    static Integer c;
    static Float d;

    public static void main(String[] args) {
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}