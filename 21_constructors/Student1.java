class Student1 {
    String name;
    int age;  
    String college;
    
    // constructor helps you to initialize properties(instance variables) of it's own class
    // you can receive the inputs in your consructor parameter-variables 
    // and now you can assign them to instance-variables
    Student1(String nm, int ag, String clg) {
        name = nm;
        age = ag;
        college = clg;
    }
}
